#!/usr/bin/env node
// Injects inline JSON-LD schema into each blog post MDX:
//   - FAQPage schema parsed from the `## FAQs` section.
//   - HowTo schema for procedural posts (hardcoded by slug).
// Idempotent: re-running replaces existing injected block via marker comments.
//
// MDX supports raw <script type="application/ld+json"> tags in body content;
// they're rendered into the HTML head/body and consumed by Google.

import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { join, basename, extname } from 'node:path';

const POSTS_DIR = join(process.cwd(), 'src/data/post');
const MARKER_START = '{/* AUTO_SCHEMA_START — generated by scripts/inject-blog-schema.mjs */}';
const MARKER_END = '{/* AUTO_SCHEMA_END */}';
const SITE = 'https://www.toptier-electrical.com';

// Procedural posts that get HowTo schema. Steps are minimal, intentionally
// pointing readers back to the post body (which has the full procedure).
// Keeping steps to 5 items aligns with Google's preferred HowTo length.
