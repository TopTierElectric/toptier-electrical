function parseFrontmatter(mdx) {
  const m = mdx.match(/^---\n([\s\S]*?)\n---/);
  if (!m) return { fm: {}, fmText: '', body: mdx };
  const out = {};
  for (const line of m[1].split('\n')) {
    const kv = line.match(/^(\w+):\s*(.*)$/);
    if (!kv) continue;
    let v = kv[2].trim();
    if (v.startsWith("'") && v.endsWith("'")) v = v.slice(1, -1);
    if (v.startsWith('"') && v.endsWith('"')) v = v.slice(1, -1);
    out[kv[1]] = v;
  }
  return { fm: out, fmText: m[0], body: mdx.slice(m[0].length) };
}

// Extract FAQs from `## FAQs` section. Each Q/A pair is:
//   **Question text?**
//   Answer paragraph (may span 1+ lines, ends at blank line or next **Q**).
function extractFAQs(body) {
  const sectionMatch = body.match(
    /##\s+FAQs?\s*\n([\s\S]*?)(?=\n##\s|\n\nReady |\n\nWant |\n\nSmelling |\n\nPlanning |\n\nWondering |\n\nSuspect |\n\nFlickering |\n\nNeed |\n\n\[|$)/
  );
  if (!sectionMatch) return [];
  const section = sectionMatch[1];
  // Match **Q?** followed by answer until next **Q** or blank line + paragraph
  const faqs = [];
  const re = /\*\*([^*\n]+\?)\*\*\s*\n([\s\S]*?)(?=\n\*\*|\n##|$)/g;
  let m;
  while ((m = re.exec(section)) !== null) {
    const question = m[1].trim();
    const answer = m[2].trim().replace(/\n+/g, ' ').replace(/\s+/g, ' ');
    if (question && answer) faqs.push({ question, answer });
  }
  return faqs;
}

function buildFAQPage(canonical, faqs) {
  if (!faqs.length) return null;
  return {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    '@id': `${canonical}#faq`,
    mainEntity: faqs.map((f) => ({
      '@type': 'Question',
      name: f.question,
      acceptedAnswer: { '@type': 'Answer', text: f.answer },
    })),
  };
}

function buildHowTo(slug, canonical) {
  const spec = HOWTO_BY_SLUG[slug];
  if (!spec) return null;
  return {
    '@context': 'https://schema.org',
    '@type': 'HowTo',
    '@id': `${canonical}#howto`,
    name: spec.name,
    description: spec.description,
    step: spec.steps.map((s, i) => ({
      '@type': 'HowToStep',
      position: i + 1,
      name: s.name,
      text: s.text,
    })),
  };
}

function buildSpeakable(canonical) {
  return {
    '@context': 'https://schema.org',
    '@type': 'WebPage',
    '@id': `${canonical}#speakable`,
    speakable: {
      '@type': 'SpeakableSpecification',
      cssSelector: ['h1', '.quick-answer', '#quick-answer'],
    },
    url: canonical,
  };
}

function renderScriptTag(obj) {
  // Use JSON.stringify twice: the first call produces the JSON string;
  // the second call wraps it as a valid JS string literal (with surrounding
  // double quotes and properly escaped internal characters). Embedded in a
  // JSX expression {…}, this round-trips back to the original JSON string,
  // which Astro then writes as the raw text content of the <script> tag.
  const jsLiteral = JSON.stringify(JSON.stringify(obj));
  return `<script type="application/ld+json">{${jsLiteral}}</script>`;
}

function stripExisting(body) {
  let out = body;
  for (;;) {
    const start = out.indexOf(MARKER_START);
    if (start === -1) break;
    const end = out.indexOf(MARKER_END, start);
    if (end === -1) break;
    out = out.slice(0, start) + out.slice(end + MARKER_END.length);
  }
  return out.replace(/\n{3,}/g, '\n\n').replace(/\s+$/, '');
}

function injectSchema(slug, content) {
  const { fmText, body } = parseFrontmatter(content);

  // Read canonical from the metadata sub-object (line-based parser misses it).
  let canon = `${SITE}/blog/${slug}`;
  const canonMatch = content.match(/canonical:\s*['"]([^'"]+)['"]/);
  if (canonMatch) canon = canonMatch[1];

  const cleaned = stripExisting(body);

  const faqs = extractFAQs(cleaned);
  const blocks = [];
  const faqPage = buildFAQPage(canon, faqs);
  if (faqPage) blocks.push(renderScriptTag(faqPage));
  const howTo = buildHowTo(slug, canon);
  if (howTo) blocks.push(renderScriptTag(howTo));
  blocks.push(renderScriptTag(buildSpeakable(canon)));

  if (!blocks.length) return content;

  const schemaSection = `\n\n${MARKER_START}\n${blocks.join('\n')}\n${MARKER_END}\n`;
  return `${fmText}${cleaned}${schemaSection}`;
}

const files = readdirSync(POSTS_DIR).filter((f) => f.endsWith('.mdx'));
let updated = 0;
for (const file of files) {
  const slug = basename(file, extname(file));
  const path = join(POSTS_DIR, file);
  const content = readFileSync(path, 'utf8');
  const next = injectSchema(slug, content);
  if (next !== content) {
    writeFileSync(path, next);
    console.log(`schema: ${slug}`);
    updated++;
  }
}
console.log(`\nInjected/refreshed schema in ${updated} post${updated === 1 ? '' : 's'}.`);
